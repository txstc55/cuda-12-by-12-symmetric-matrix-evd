template <unsigned int n>
__device__ __forceinline__ void
qr_tri_diagonal(double A[n * n], double E[n * n], bool updateEigenVectors) {
  double Q[n * n];
  double R[n * n];
  for (int i = 0; i < n * n; ++i) {
    Q[i] = 0.0;
    R[i] = 0.0;
  }
  double ACopy[n * n];
  for (int i = 0; i < n * n; ++i) {
    ACopy[i] = A[i];
  }

  double eq[n * n];
  double e_tmp, q_tmp;

  for (unsigned int iteration = 0; iteration < n * 2 - 1; iteration++) {
    compute_qr_tri_diagonal<n>(ACopy, Q, R);
    bool continueQR = true;
    for (unsigned int i = 0; i < n; i++) {
      if (fabs(R[i * n + i]) < 1e-6) {
        continueQR = false;
        break;
      }
    }

    if (!continueQR) {
      break;
    }

    // compute ACopy = R * Q
    unsigned int i = 0;
    unsigned int j = 0;

    i = 0;
    j = 0;
    ACopy[i * n + j] = 0.0;
    for (unsigned int k = i; k < n; k++) {
      ACopy[i * n + j] += R[i * n + k] * Q[k * n + j];
    }
    i = 0;
    j = 1;
    ACopy[i * n + j] = 0.0;
    for (unsigned int k = i; k < n; k++) {
      ACopy[i * n + j] += R[i * n + k] * Q[k * n + j];
    }
    ACopy[j * n + i] = ACopy[i * n + j];

    for (i = 1; i < 11; i++) {
      j = i;
      ACopy[i * n + j] = 0.0;
      for (unsigned int k = i; k < n; k++) {
        ACopy[i * n + j] += R[i * n + k] * Q[k * n + j];
      }
      j += 1;
      ACopy[i * n + j] = 0.0;
      for (unsigned int k = i; k < n; k++) {
        ACopy[i * n + j] += R[i * n + k] * Q[k * n + j];
      }
      ACopy[j * n + i] = ACopy[i * n + j];
    }
    i = 11;
    j = 11;
    ACopy[i * n + j] = 0.0;
    for (unsigned int k = i; k < n; k++) {
      ACopy[i * n + j] += R[i * n + k] * Q[k * n + j];
    }

    if (updateEigenVectors) {
      // E = E * Q
      for (unsigned int i = 0; i < n; i++) {
        for (unsigned int j = 0; j < n; j++) {
          double sum = 0.0;
          for (unsigned int k = 0; k < n; k++) {
            e_tmp = E[i * n + k];
            q_tmp = Q[k * n + j];
            sum += e_tmp * q_tmp;
          }
          eq[i * n + j] = sum;
        }
      }
      for (unsigned int i = 0; i < n * n; i++) {
        E[i] = eq[i];
      }
    }
  }

  // now we do the jacobi iterations
  bool keepDoingJacobian = true;
  while (keepDoingJacobian) {
    find_pivot_and_rotate(ACopy, n, E, updateEigenVectors);
    keepDoingJacobian = false;
    for (int i = 0; i < n; i++) {
      for (int j = i + 1; j < n; j++) {
        double absv = fabsf(ACopy[i * n + j]);
        if (absv > 1e-6) {
          keepDoingJacobian = true;
          break;
        }
      }
    }
  }

  for (unsigned int i = 0; i < n; i++) {
    A[i * n + i] = ACopy[i * n + i];
  }
}